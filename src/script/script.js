let lang = navigator.language.substr(0, 2) == "en" || navigator.language.substr(0, 2) == "pt" || navigator.language.substr(0, 2) == "fr" ? navigator.language.substr(0, 2) : "pt"

document.addEventListener('DOMContentLoaded', () => {
    languagesMatch(lang)
    translate()
    fitNavMargin()
});

var newLang = document.getElementsByClassName("languages")

addEventListener("click", (newLang) => {
    newLang = newLang.srcElement.firstChild.data
    languagesMatch(newLang.toLowerCase())
    translate()
    fitNavMargin()
})

function fitNavMargin() {
    var navElements = document.getElementsByClassName('nav-element');
    var pipanav = document.getElementsByClassName('pipanav');
    for (var i = 0; i < pipanav.length; i++) {
        pipanav[i].style.marginLeft = -1 * navElements[i].offsetWidth - 30 + "px"
    }
}

function translate() {
    let zones = document.querySelectorAll('html [lang-key]');
    let imgs = document.querySelectorAll('html [img-key]');
    applyStrings(zones);
    applyImgsSrc(imgs);
}

function applyStrings(containers) {
    containers.forEach(container => {
        let key = container.getAttribute('lang-key');
        if (key) {
            container.innerHTML = currentLanguages[key];
        }
    })
}

function applyImgsSrc(container) {
    for (i = 0; i < container.length; i++) {
        let key = container[i].getAttribute('img-key');
        if (key) {
            container[i].src = currentLanguages[key];
        }
    }
}

function languagesMatch(lang) {
    currentLanguages = languages[lang];
}


